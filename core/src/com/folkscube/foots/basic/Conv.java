package com.folkscube.foots.basic;

public class Conv {
	public static float getcx(float lx,float radius){
		return lx+radius;
	}
	public static float getcy(float ly,float radius){
		return ly+radius;
	}
	public static float getrx(float x,float w){
		return x+(w/2);
	}
	public static float getry(float y,float h){
		return y+(h/2);
	}
}
